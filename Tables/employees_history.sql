CREATE TABLE employees_history  (   employee_id NUMBER,
                                    first_name VARCHAR2(25), 
                                    last_name VARCHAR2(25), 
                                    email VARCHAR2(35), 
                                    phone_number VARCHAR2(15),
                                    start_date DATE,
                                    end_date DATE ,
                                    job_id VARCHAR2(10),
                                    department_id NUMBER );
